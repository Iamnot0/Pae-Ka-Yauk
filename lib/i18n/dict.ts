export type DictKey = keyof typeof dict;

export function tr(key: DictKey, locale: Locale = 'my'): string {
  const entry = dict[key];
  return entry?.[locale] ?? dict[key]?.en ?? key;
}

/**
 * Returns both English and Myanmar strings for a key, regardless of the
 * current UI locale. Used by the printed-slip renderer, which is always
 * bilingual (shopkeeper wants both scripts on every receipt).
 */
export function trBoth(key: DictKey): { en: string; my: string } {
  const entry = dict[key];
  return { en: entry?.en ?? String(key), my: entry?.my ?? '' };
}
